#define HCMS_CS  15
#define HCMS_DIN 13
#define HCMS_RS  12
#define HCMS_CLK 14
//==============================================================================
void Load_HCMS (char aSym1, char aSym2, char aSym3, char aSym4)
{
 digitalWrite(HCMS_RS, LOW); digitalWrite(HCMS_CS, LOW);
 
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym1*5 + 0]); 
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym1*5 + 1]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym1*5 + 2]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym1*5 + 3]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym1*5 + 4]);

 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym2*5 + 0]); 
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym2*5 + 1]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym2*5 + 2]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym2*5 + 3]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym2*5 + 4]);

 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym3*5 + 0]); 
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym3*5 + 1]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym3*5 + 2]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym3*5 + 3]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym3*5 + 4]);

 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym4*5 + 0]); 
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym4*5 + 1]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym4*5 + 2]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym4*5 + 3]);
 shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, ASCII[aSym4*5 + 4]); 

 digitalWrite(HCMS_CS, HIGH);
}
//==============================================================================
void Config_HCMS(unsigned char aCW0, unsigned char aCW1)
{
  digitalWrite(HCMS_RS, HIGH); digitalWrite(HCMS_CS, LOW); shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, aCW0); digitalWrite(HCMS_CS, HIGH);
  digitalWrite(HCMS_RS, HIGH); digitalWrite(HCMS_CS, LOW); shiftOut(HCMS_DIN, HCMS_CLK, MSBFIRST, aCW1); digitalWrite(HCMS_CS, HIGH);
}
//==================================================================================================
void HCMS_init()
{
  pinMode(HCMS_CS, OUTPUT);
  pinMode(HCMS_DIN, OUTPUT);
  pinMode(HCMS_RS, OUTPUT);
  pinMode(HCMS_CLK, OUTPUT);

  digitalWrite(HCMS_CS, LOW);
  digitalWrite(HCMS_DIN, LOW);
  digitalWrite(HCMS_RS, LOW);
  digitalWrite(HCMS_CLK, LOW);
  Config_HCMS(0x7f, 0x80);  
}
